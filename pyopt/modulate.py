def image_to_binary(image_decimal):
    image_binary = np.zeros(len(image_decimal) * 8, dtype=int)
    print('image to binary START')
    for byte in tqdm(range(len(image_decimal))):
        for bit in range(8):
            image_binary[byte * 8 + bit] = min(1 << (7 - bit) & image_decimal[byte], 1)
    return image_binary


def nrzqpsk(bitsq, n, equalize=False):
    symbol_num = int(len(bitsq) / 2)
    tmp = bitsq
    data = np.zeros(symbol_num * n, dtype=complex)
    print('nrzqpsk modulation START')
    for i in tqdm(range(symbol_num)):
        if tmp[i * 2] == 1 and tmp[i * 2 + 1] == 1:
            Q = np.pi / 4
        elif tmp[i * 2] == 0 and tmp[i * 2 + 1] == 1:
            Q = np.pi * 3 / 4
        elif tmp[i * 2] == 0 and tmp[i * 2 + 1] == 0:
            Q = -np.pi * 3 / 4
        else:
            Q = -np.pi / 4
        data[i * n: i * n + n] = np.exp(1j * Q)
    return data


def rzqpsk(bitsq, n, equalize=False):  # 平均入力パワー
    symbol_num = int(len(bitsq) / 2)
    tmp = (bitsq - 0.5) * 2
    tmp_i = tmp[::2]
    tmp_q = tmp[1::2]
    data_i = np.zeros(symbol_num * n, dtype=float)
    data_q = np.zeros(symbol_num * n, dtype=float)
    print('rzqpsk modulation START')
    for i in tqdm(range(symbol_num)):
        for j in range(n):
            data_i[i * n + j] = tmp_i[i] * np.sin(np.pi * j / n)
            data_q[i * n + j] = tmp_q[i] * np.sin(np.pi * j / n)
    data = (data_i + 1j * data_q)

    return data


